from colorama import Fore as F
from math import sqrt
from queue import PriorityQueue


class Node:
    def __init__(self, x: int, y: int) -> None:
        self.x = x
        self.y = y

        self.g = 0
        self.h = 0
        self.f = self.g + self.h

        self.neighbors = []
        self.parent = None

    def get_neighbors(self) -> list:
        for i in range(-1, 2):
            for j in range(-1, 2):
                if i != 0 or j != 0:
                    self.neighbors.append(Node(self.x + i, self.y + j))

        return self.neighbors

    def __eq__(self, __o) -> bool:
        return (self.x, self.y) == (__o.x, __o.y)

    def __lt__(self, __o) -> bool:
        return self.f < __o.f

    def __str__(self) -> str:
        return f"({self.x},{self.y})"


class AStar:
    def __init__(self, maze: list, start: Node, target: Node) -> None:
        self.maze = maze
        self.path = maze
        self.start = start
        self.target = target
        self.open_list = PriorityQueue()
        self.closed_list = []
        self.steps = []

    def solve(self) -> None:
        x_limit = len(self.maze)
        y_limit = len(self.maze[0])

        self.open_list.put(self.start)

        while not self.open_list.empty():
            current = self.open_list.get()
            self.closed_list.append(current)

            if current == self.target:
                self.target.parent = current.parent
                self.retrace_path()
                self.show()
                return

            for neighbor in current.get_neighbors():
                if (
                    not (
                        0 <= neighbor.x < x_limit
                        and 0 <= neighbor.y < y_limit
                        and self.maze[neighbor.x][neighbor.y] != 1
                    )
                    or neighbor in self.closed_list
                ):
                    continue

                new_cost = current.g + self.get_distance(current, neighbor)

                if new_cost < neighbor.g or neighbor not in self.open_list.queue:
                    neighbor.g = new_cost
                    neighbor.h = self.get_distance(neighbor, self.target)
                    neighbor.f = neighbor.g + neighbor.h
                    neighbor.parent = current

                    if neighbor not in self.open_list.queue:
                        self.open_list.put(neighbor)

    def get_distance(self, a: Node, b: Node) -> float:
        return sqrt((a.x - b.x) ** 2 + (a.y - b.y) ** 2)

    def retrace_path(self) -> None:
        current = self.target

        while not current == self.start:
            self.steps.append(current)
            current = current.parent

        self.steps.reverse()

    def show(self) -> None:

        print(f"{ F.RED }- - -  A* - - -")
        print(f"{ F.CYAN }Cantidad de pasos: { F.WHITE }{ len(self.steps) }")
        print(f"{ F.CYAN }Ruta: {F.RESET}{' -> '.join(map(str, self.steps))}")

        self.path[self.start.x][self.start.y] = "s"
        self.path[self.target.x][self.target.y] = "e"

        for i in range(len(self.path)):
            for j in range(len(self.path[i])):
                if (
                    Node(i, j) in self.steps
                    and self.path[i][j] != "s"
                    and self.path[i][j] != "e"
                ):
                    print(f"{ F.CYAN }*", end=" ")
                elif self.path[i][j] == 1:
                    print(f"{ F.WHITE }#", end=" ")
                elif self.path[i][j] == 0:
                    print(f"{ F.GREEN }.", end=" ")
                else:
                    print(f"{ F.RED }{ self.path[i][j] }", end=" ")
            print()


